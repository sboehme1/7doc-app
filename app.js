if ("serviceWorker" in navigator) {
  function showUpdateToast() {
    var toast = document.createElement('div');
    toast.className = 'update-toast';
    toast.innerHTML = '<span class="update-toast-icon">↻</span><span>Neue Version verfügbar — App wird aktualisiert.</span>';
    document.body.appendChild(toast);
    setTimeout(function(){ toast.classList.add('visible'); }, 100);
    setTimeout(function(){
      toast.classList.remove('visible');
      setTimeout(function(){ toast.remove(); }, 400);
    }, 4000);
  }
  navigator.serviceWorker.register("./sw.js").then(function(reg) {
    reg.addEventListener("updatefound", function() {
      var newSW = reg.installing;
      if (!newSW) return;
      newSW.addEventListener("statechange", function() {
        if (newSW.state === "installed" && navigator.serviceWorker.controller) {
          /* Neuer SW wartet — Update-Banner anzeigen */
          var banner = document.getElementById("update-banner");
          var txt = document.getElementById("update-text");
          var confirmBtn = document.getElementById("update-confirm");
          var dismissBtn = document.getElementById("update-dismiss");
          if (!banner) return;
          if (txt) txt.textContent = t("updateMsg");
          if (confirmBtn) confirmBtn.textContent = t("updateConfirm");
          if (dismissBtn) dismissBtn.textContent = t("updateDismiss");
          if (confirmBtn) confirmBtn.onclick = function() {
            if (newSW) newSW.postMessage({ type: "SKIP_WAITING" });
            banner.style.display = "none";
          };
          if (dismissBtn) dismissBtn.onclick = function() {
            banner.style.display = "none";
          };
          banner.style.display = "flex";
        }
      });
    });
  });
  /* Reload auslösen wenn neuer SW die Kontrolle übernimmt */
  if (!sessionStorage.getItem('7doc_update_shown')) {
    sessionStorage.setItem('7doc_update_shown', '1');
    showUpdateToast();
  }
  navigator.serviceWorker.addEventListener("controllerchange", function() {
    if(!sessionStorage.getItem("7doc_reloaded")){
      sessionStorage.setItem("7doc_reloaded","1");
      window.location.reload();
    }
  });
}

